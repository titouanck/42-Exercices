#include <string.h>

int	ft_strequ(char const *s1, char const *s2)
{
	size_t	i;
	int		r;

	i = 0;
	while (s1[i] && s1[i] == s2[i])
		i++;
	r = s1[i] - s2[i];
	if (r == 0)
		return (1);
	return (0);
}
