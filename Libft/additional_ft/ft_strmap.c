#include "libft.h"


char	*ft_strmap(char const *s, char (*f)(char))
{
	size_t	len;
	char	*str;
	size_t	i;

	len = ft_strlen(s);
	str = (char *) malloc(sizeof(char) * (len + 1));
	if (str == NULL)
		return (0);
	i = 0;
	while (i < len)
	{
		str[i] = f(s[i]);
		i++;
	}
	str[len] = '\0';
	return (str);
}
